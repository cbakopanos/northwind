CREATE SCHEMA supplier;

CREATE TABLE supplier.suppliers (
    supplier_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_name varchar(40) NOT NULL,
    contact_name varchar(30),
    contact_title varchar(30),
    address varchar(60),
    city varchar(15),
    region varchar(15),
    postal_code varchar(10),
    country varchar(15),
    phone varchar(24),
    fax varchar(24),
    homepage_url text
);

CREATE INDEX suppliers_company_name_idx
    ON supplier.suppliers (company_name);

CREATE INDEX suppliers_postal_code_idx
    ON supplier.suppliers (postal_code);
